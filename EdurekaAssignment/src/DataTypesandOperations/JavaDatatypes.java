package DataTypesandOperations;

public class JavaDatatypes {

	public static void main(String[] args) {
		byte a=1;
		short b=2;
		int c=3;
		long d=4;
		float e=5.01f;
		double f=45.876f;
		char g='v';
		boolean h=true;
		
		System.out.println("adding a and b :"+(a+b));
		System.out.println("substracting c from b :"+ (c-b));
		System.out.println("multiplicating d and c :"+ (d*c));
		System.out.println("divding d and b :"+ (d/b));
		//incrementing a
		a++;
		System.out.println("After incrementing a:" +a);
		//decrementig a
		a--;
		System.out.println("After decrementing a:" +a);
	}

}
